# Arithmetic Operations using input functions

a = float(input("enter the first number a: "))
b = float(input("enter the second number b: "))
print("Sum:\n", a + b)
print("Sub:\n", a - b)
print("Multi:\n", a * b)
print("Divide:\n", a / b)
print("floor division:\n", a // b)
print("Modulo:\n", a % b)
print("exponent:\n" , a ** b)